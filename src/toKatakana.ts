
export function toKatakana(value?: string) {
  if (!value) {
    return null
  }

  let result = ""
  for (let i = 0; i < value.length; i++) {
    const c = value.charAt(i)
    if (i + 1 < value.length) {
      const c2 = value.charAt(i+1)
      if (c2 >= "\u3099") {
        result += toKatakanaChar(c + c2)
        i++
        continue
      }
    }
    result += toKatakanaChar(c)
  }
  return result
}

